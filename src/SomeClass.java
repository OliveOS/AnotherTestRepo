public class SomeClass {
    private int data;
    private String word;
    private char val;

    public SomeClass(int data, String word, char val) {
        this.data = data;
        this.word = word;
        this.val = val;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public char getVal() {
        return val;
    }

    public void setVal(char val) {
        this.val = val;
    }
}
